/**
 * faq-store.service.ts
 *
 * Persistent FAQ store. Every approved Q&A pair is stored here with:
 *  - its question embedding (local bge-small)
 *  - embeddings for each rephrasing
 *  - BM25 token data
 *
 * Persisted to data/faq-store.json on disk. Loaded into memory at startup.
 * Zero network calls at query time.
 */

import fs from 'fs';
import path from 'path';
import { LocalTransformersEmbeddings } from './embedding.service';

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export type PairStatus = 'pending' | 'approved' | 'rejected';

export interface FaqPair {
  id: string;
  question: string;
  rephrasings: string[];
  answer: string;
  source: string;       // pdfName
  chunk_ref: string;    // chunkIndex as string
  grounded_quote: string;
  status: PairStatus;
  isAutoGenerated?: boolean;
  thumbsUp?: number;
  thumbsDown?: number;
  // Populated after indexing:
  question_embedding?: number[];
  rephrasing_embeddings?: number[][];
  question_tokens?: string[];
  rephrasing_tokens?: string[][];
}

interface StoredDoc {
  id: string;           // faqPairId
  text: string;         // the question or rephrasing text
  embedding: number[];
  tokens: string[];
  termFrequencies: Record<string, number>;
}

interface FaqStoreData {
  pairs: FaqPair[];
}

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

const DATA_DIR  = path.join(process.cwd(), 'data');
const STORE_FILE = path.join(DATA_DIR, 'faq-store.json');

// BM25 parameters
const BM25_K1 = 1.5;
const BM25_B  = 0.75;

// Hybrid RRF fusion weights (same as original vector.service.ts)
const WEIGHT_SEMANTIC = 0.7;
const WEIGHT_BM25     = 0.3;
const RRF_K           = 60;

// ---------------------------------------------------------------------------
// Tokenizer (identical to vector.service.ts)
// ---------------------------------------------------------------------------

function tokenize(text: string): string[] {
  if (!text || typeof text !== 'string') return [];
  return text.toLowerCase().match(/\b\w+\b/g) || [];
}

// ---------------------------------------------------------------------------
// In-memory state
// ---------------------------------------------------------------------------

let _pairs: FaqPair[] = [];
let _docs: StoredDoc[] = [];

// BM25 corpus stats (rebuilt whenever docs change)
let _avgdl = 0;
let _idf: Record<string, number> = {};

// Singleton embedder (shared across calls in the same process)
let _embedder: LocalTransformersEmbeddings | null = null;

export function getEmbedder(): LocalTransformersEmbeddings {
  if (!_embedder) {
    _embedder = new LocalTransformersEmbeddings();
  }
  return _embedder;
}

// ---------------------------------------------------------------------------
// Disk I/O
// ---------------------------------------------------------------------------

function ensureDataDir() {
  if (!fs.existsSync(DATA_DIR)) {
    fs.mkdirSync(DATA_DIR, { recursive: true });
  }
}

export function loadStore(): void {
  ensureDataDir();
  if (!fs.existsSync(STORE_FILE)) {
    _pairs = [];
    _docs  = [];
    console.log('[FaqStore] No store file found — starting fresh.');
    return;
  }

  try {
    const raw  = fs.readFileSync(STORE_FILE, 'utf-8');
    const data = JSON.parse(raw);
    _pairs = data.pairs ?? [];
    
    // Sanitize pairs on load to ensure data is clean even if not re-uploaded
    sanitizePairs();

    // Re-hydrate in-memory search docs from stored embeddings
    _docs = [];
    for (const pair of _pairs) {
      if (pair.status !== 'approved') continue;
      if (!pair.question_embedding) continue;

      // Question doc
      const qTokens = tokenize(pair.question);
      _docs.push({
        id: pair.id,
        text: pair.question,
        embedding: pair.question_embedding,
        tokens: qTokens,
        termFrequencies: buildTermFreq(qTokens),
      });

      // Rephrasing docs
      if (pair.rephrasing_embeddings) {
        for (let i = 0; i < pair.rephrasings.length; i++) {
          const rTokens = tokenize(pair.rephrasings[i]);
          _docs.push({
            id: pair.id,
            text: pair.rephrasings[i],
            embedding: pair.rephrasing_embeddings[i] ?? pair.question_embedding,
            tokens: rTokens,
            termFrequencies: buildTermFreq(rTokens),
          });
        }
      }
    }

    if (_docs.length === 0 && _pairs.length > 0) {
      _rebuildSearchDocs();
    } else {
      _recalcBM25();
    }

    console.log(`[FaqStore] Loaded ${_pairs.length} pairs (${_docs.length} search docs) from disk.`);
  } catch (e) {
    console.error('[FaqStore] Failed to load store from disk:', e);
    _pairs = [];
    _docs  = [];
  }
}

export function saveStore(): void {
  ensureDataDir();
  const data: FaqStoreData = { pairs: _pairs };
  fs.writeFileSync(STORE_FILE, JSON.stringify(data, null, 2), 'utf-8');
}

// ---------------------------------------------------------------------------
// BM25 helpers
// ---------------------------------------------------------------------------

function buildTermFreq(tokens: string[]): Record<string, number> {
  const tf: Record<string, number> = {};
  for (const t of tokens) tf[t] = (tf[t] ?? 0) + 1;
  return tf;
}

function _recalcBM25(): void {
  const N = _docs.length;
  if (N === 0) { _avgdl = 0; _idf = {}; return; }

  let totalLen = 0;
  const df: Record<string, number> = {};

  for (const doc of _docs) {
    totalLen += doc.tokens.length;
    for (const t of Object.keys(doc.termFrequencies)) {
      df[t] = (df[t] ?? 0) + 1;
    }
  }

  _avgdl = totalLen / N;
  _idf = {};
  for (const t in df) {
    const n = df[t];
    _idf[t] = Math.log((N - n + 0.5) / (n + 0.5) + 1);
  }
}

function _bm25Score(queryTokens: string[], doc: StoredDoc): number {
  let score = 0;
  const dl = doc.tokens.length;

  for (const q of queryTokens) {
    const tf  = doc.termFrequencies[q] ?? 0;
    if (tf === 0) continue;
    const idf = _idf[q] ?? 0;
    const num = tf * (BM25_K1 + 1);
    const den = tf + BM25_K1 * (1 - BM25_B + BM25_B * (dl / _avgdl));
    score += idf * (num / den);
  }
  return score;
}

// ---------------------------------------------------------------------------
// Cosine similarity
// ---------------------------------------------------------------------------

function cosineSimilarity(a: number[], b: number[]): number {
  let dot = 0, na = 0, nb = 0;
  for (let i = 0; i < a.length; i++) {
    dot += a[i] * b[i];
    na  += a[i] * a[i];
    nb  += b[i] * b[i];
  }
  if (na === 0 || nb === 0) return 0;
  return dot / (Math.sqrt(na) * Math.sqrt(nb));
}

